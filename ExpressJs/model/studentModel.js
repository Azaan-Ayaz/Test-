class Student {
    constructor(name, age, email, phone, address, grade, course, DOB, enrollment_date, is_active){
        this.name = name;
        this.age = age;
        this.email = email;
        this.phone = phone;
        this.address = address;
        this.grade = grade;
        this.course = course;
        this.DOB = DOB;
        this.enrollment_date = enrollment_date;
        this.is_active = is_active
    }
}

module.exports = Student